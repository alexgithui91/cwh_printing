def print_string():
    # Printing simple string
    print("Welcome to Coding With Hitz!")
    # Print blank line
    print("\n")


def print_cars():
    # Printing Cars
    print("Toyota")
    print("Hummer")
    print("Cadillac")
    # Print blank line
    print("\n")


def print_with_end():
    # Printing with end
    print("This is", end="---")
    print("Coding With", end="---")
    print("Hitz", end="!!")
    # Print blank line
    print("\n")
    print("Home of programming tutorials", end=":-)")


def main():
    print_string()
    print_cars()
    print_with_end()


if __name__ == "__main__":
    main()
